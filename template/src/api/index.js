import $axios from "./http.service";

// --------------------------------------------------------
// 以下为调用接口获取后端数据方法的区域
// 如果方法调用分模块处理，则可以在apis目录下创建不同的模块
// 在 index.js 文件中统一引入。
// --------------------------------------------------------
// 示例：
/** 获取用户评论的信息列表 */
export const getUserCommentList = function (params) {
  return $axios({
    url: '/user-comment-list',
    params: params
  })
}
